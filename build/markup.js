export function markAsMax(value) {
    return `<b>${value}</b>`;
}
export function markAsMin(value) {
    return `<i>${value}</i>`;
}
export function markAsDropped(value) {
    return `<s>${value}</s>`;
}
export function markAsFixed(value) {
    return `${value}f`;
}
export function markExploded(value) {
    return `${value}ₓ`;
}
export function markAsExplosion(value) {
    return `${value}⁺`;
}
